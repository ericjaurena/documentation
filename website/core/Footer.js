const React = require('react');

class Footer extends React.Component {
  docUrl(doc, language) {
    const baseUrl = this.props.config.baseUrl;
    return `${baseUrl}docs/${language ? `${language}/` : ''}${doc}`;
  }

  pageUrl(doc, language) {
    const baseUrl = this.props.config.baseUrl;
    return baseUrl + (language ? `${language}/` : '') + doc;
  }

  render() {
    return (
      <footer className="nav-footer" id="footer">
        <section className="sitemap">
          <a href={this.props.config.baseUrl} className="nav-home">
            {this.props.config.footerIcon && (
              <img
                src={this.props.config.baseUrl + this.props.config.footerIcon}
                alt={this.props.config.title}
                width="66"
                height="58"
              />
            )}
          </a>
          <div>
            <h5>Docs</h5>
            <a href={this.docUrl('overview.html', this.props.language)}>
              Chainlinks
            </a>
            <a href={this.docUrl('rpc_ropsten.html', this.props.language)}>
              Public Ethereum RPCs
            </a>
            <a href={this.docUrl('terraform.html', this.props.language)}>
              Infrastructure Design
            </a>
          </div>
          <div>
            <h5>Community</h5>
            <a
              href="https://t.me/linkpoolio"
              target="_blank"
              rel="noreferrer noopener">
              Telegram
            </a>
            <a
              href="https://twitter.com/linkpoolio"
              target="_blank"
              rel="noreferrer noopener">
              Twitter
            </a>
          </div>
          <div>
            <h5>More</h5>
            <a href={this.props.config.baseUrl + "contact"}>Contact us</a>
            <a href="https://linkpool.io">Website</a>
            <a href="https://staking.linkpool.io">App</a>
          </div>
        </section>

        <a
          href="https://code.facebook.com/projects/"
          target="_blank"
          rel="noreferrer noopener"
          className="fbOpenSource">
          <img
            src={`${this.props.config.baseUrl}img/oss_logo.png`}
            alt="Facebook Open Source"
            width="170"
            height="45"
          />
        </a>
        <section className="copyright">{this.props.config.copyright}</section>
      </footer>
    );
  }
}

module.exports = Footer;
